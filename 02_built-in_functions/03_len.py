# Longitud de una cadena de texto
cadena = "Programación Python"
print(f"La longitud de '{cadena}' es: {len(cadena)}") # Salida: 19

# Longitud de una lista
mi_lista = [10, 20, 30, 40, 50, 60]
print(f"La longitud de mi_lista es: {len(mi_lista)}") # Salida: 6

# Longitud de una tupla
mi_tupla = ("a", "b", "c")
print(f"La longitud de mi_tupla es: {len(mi_tupla)}") # Salida: 3

# Longitud de un diccionario (número de pares clave-valor)
mi_diccionario = {"uno": 1, "dos": 2, "tres": 3}
print(f"La longitud de mi_diccionario es: {len(mi_diccionario)}") # Salida: 3

# Longitud de un conjunto
mi_conjunto = {1, 2, 3, 3, 4} # Los elementos duplicados se ignoran en conjuntos
print(f"La longitud de mi_conjunto es: {len(mi_conjunto)}") # Salida: 4