# Operadores Aritméticos

# Suma (+)
a = 10
b = 5
suma = a + b
print(f"Suma de {a} + {b} = {suma}")  # Salida: Suma de 10 + 5 = 15

# Resta (-)
resta = a - b
print(f"Resta de {a} - {b} = {resta}")  # Salida: Resta de 10 - 5 = 5

# Multiplicación (*)
multiplicacion = a * b
print(f"Multiplicación de {a} * {b} = {multiplicacion}")  # Salida: Multiplicación de 10 * 5 = 50

# División (/)
division = a / b
print(f"División de {a} / {b} = {division}")  # Salida: División de 10 / 5 = 2.0 (siempre float)

# Módulo (%) - Resto de la división
modulo = a % b
print(f"Módulo de {a} % {b} = {modulo}")  # Salida: Módulo de 10 % 5 = 0

# Exponenciación (**)
exponenciacion = a ** 2
print(f"Exponenciación de {a} ** 2 = {exponenciacion}")  # Salida: Exponenciación de 10 ** 2 = 100

# División Entera (//) - Cociente de la división, sin decimales
division_entera = a // b
print(f"División Entera de {a} // {b} = {division_entera}")  # Salida: División Entera de 10 // 5 = 2

# Con números flotantes
x = 7
y = 2
print(f"División Entera de {x} // {y} = {x // y}") # Salida: División Entera de 7 // 2 = 3