a = True
b = False
c = True
d = False

# and
print(f"a and b: {a and b}")  # Salida: a and b: False
print(f"a and c: {a and c}")  # Salida: a and c: True
print(f"b and d: {b and d}")  # Salida: b and d: False

# or
print(f"a or b: {a or b}")    # Salida: a or b: True
print(f"b or d: {b or d}")    # Salida: b or d: False
print(f"c or d: {c or d}")    # Salida: c or d: True

# not
print(f"not a: {not a}")      # Salida: not a: False
print(f"not b: {not b}")      # Salida: not b: True
print(f"not c: {not c}")      # Salida: not c: False
print(f"not d: {not d}")      # Salida: not d: True